// ==================== SEED FUNCTION ====================

async function seedAllPrompts() {
  console.log('=== Comprehensive Prompt Registry Seed ===\n');

  const allPrompts = AI_SERVICE_PROMPTS;

  let created = 0;
  let updated = 0;
  let skipped = 0;
  let depCreated = 0;

  for (const def of allPrompts) {
    try {
      // Check if prompt already exists
      const [existing] = await db.select()
        .from(promptRegistry)
        .where(eq(promptRegistry.promptKey, def.promptKey))
        .limit(1);

      if (existing) {
        // Update metadata fields if they're new
        const needsUpdate = !existing.department || !existing.promptFunction || !existing.purpose;
        if (needsUpdate) {
          await db.update(promptRegistry)
            .set({
              department: def.department,
              promptFunction: def.promptFunction,
              purpose: def.purpose,
              aiModel: def.aiModel,
              status: def.status,
              invocationPoint: def.invocationPoint,
              inputDependencies: def.inputDependencies,
              outputDestination: def.outputDestination,
              sourceFile: def.sourceFile,
              sourceExport: def.sourceExport,
              tags: def.tags,
              updatedAt: new Date(),
            })
            .where(eq(promptRegistry.id, existing.id));
          updated++;
          console.log(`  Updated: ${def.promptKey}`);
        } else {
          skipped++;
        }
      } else {
        // Create new prompt
        const [inserted] = await db.insert(promptRegistry)
          .values({
            promptKey: def.promptKey,
            name: def.name,
            description: def.description,
            promptType: def.promptType,
            promptScope: def.promptScope,
            agentType: def.agentType || null,
            category: def.category,
            content: def.content,
            defaultContent: def.content,
            isActive: true,
            isLocked: false,
            priority: 50,
            tags: def.tags,
            sourceFile: def.sourceFile,
            sourceExport: def.sourceExport,
            department: def.department,
            promptFunction: def.promptFunction,
            purpose: def.purpose,
            aiModel: def.aiModel,
            status: def.status,
            invocationPoint: def.invocationPoint,
            inputDependencies: def.inputDependencies,
            outputDestination: def.outputDestination,
          })
          .returning();

        created++;
        console.log(`  Created: ${def.promptKey}`);

        // Create dependency mappings
        if (inserted && def.dependencies.length > 0) {
          for (const dep of def.dependencies) {
            await db.insert(promptDependencyMap)
              .values({
                promptId: inserted.id,
                entityType: dep.entityType,
                entityName: dep.entityName,
                endpointPath: dep.endpointPath || null,
                httpMethod: dep.httpMethod || null,
                serviceFunction: dep.serviceFunction,
                direction: dep.direction,
              });
            depCreated++;
          }
        }
      }
    } catch (error: any) {
      console.error(`  Error seeding ${def.promptKey}:`, error.message);
    }
  }

  console.log(`\n=== Seed Complete ===`);
  console.log(`  Created: ${created}`);
  console.log(`  Updated: ${updated}`);
  console.log(`  Skipped: ${skipped}`);
  console.log(`  Dependencies created: ${depCreated}`);
  console.log(`  Total prompts defined: ${allPrompts.length}`);
}

// Run
seedAllPrompts()
  .then(() => {
    console.log('\nDone.');
    process.exit(0);
  })
  .catch((err) => {
    console.error('Seed failed:', err);
    process.exit(1);
  });
