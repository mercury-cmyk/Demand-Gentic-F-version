  promptKey: string;
  name: string;
  description: string;
  promptType: 'foundational' | 'system' | 'specialized' | 'template';
  promptScope: 'global' | 'organization' | 'campaign' | 'agent_type';
  agentType?: string;
  category: 'voice' | 'email' | 'intelligence' | 'compliance' | 'system';
  department: 'sales' | 'marketing' | 'operations' | 'ai_engineering' | 'crm' | 'compliance' | 'intelligence' | 'content';
  promptFunction: 'email_drafting' | 'call_script' | 'lead_scoring' | 'enrichment' | 'campaign_personalization' | 'classification' | 'summarization' | 'reasoning' | 'routing' | 'research' | 'content_generation' | 'quality_analysis' | 'disposition' | 'mapping' | 'simulation' | 'image_generation';
